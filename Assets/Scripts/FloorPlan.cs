using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class FloorPlan {

	private int x;
	private int z;

	private int width;
	private int height;

	public FloorPlan(int x, int z, int width, int height) {
		this.x = x;
		this.z = z;
		this.width = width;
		this.height = height;
	}
}
